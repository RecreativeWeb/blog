Sur la frange nord de la chaîne de l’Alaska, juste avant que le rempart imposant du
mont McKinley et de ses satellites ne laisse place à la plaine de la Kantishna, une série de
sommets de moindre importance – connue sous le nom de Chaîne Extérieure – descend
vers les étendues planes comme une couverture plissée sur un lit défait. Entre les arêtes
siliceuses des deux derniers escarpements de cette Chaîne Extérieure il y a une combe
d’environ huit kilomètres que recouvre un amalgame bourbeux de marécages, de groupes
d’aulnes et d’alignements d’épicéas chétifs. La piste Stampede y serpente sur un terrain
ondulant et touffu : c’est le chemin que suivit Chris McCandless pour s’enfoncer dans
cette terre inhabitée.
Cette piste a été tracée dans les années 1930 par un célèbre mineur nommé Earl
Pilgrim. Elle conduit à des concessions d’antimoine qu’il possédait à la cluse de
Stampede, en amont de la fourche de Clearwater sur la rivière Toklat. En 1961, une société
de Fairbanks, la Yutan Construction, fut choisie par le nouvel État d’Alaska (l’accession
au statut d’État datait d’à peine deux ans) pour transformer la piste en une route que les
camions pourraient emprunter tout au long de l’année pour transporter le minerai. Afin
de loger les ouvriers pendant les travaux, la Yutan acheta trois autobus hors d’usage, les
équipa de couchettes et d’un simple poêle et les fit remorquer par un tracteur dans cette
contrée déserte et sauvage.
En 1963, le projet fut abandonné. Quelque 80 kilomètres de cette route avaient été
construits, mais sans aucun pont pour traverser les nombreux cours d’eau qu’elle croise.
Aussi fut-elle rapidement rendue impraticable par la fonte de la couche supérieure du
permafrost et par les inondations saisonnières. La Yutan ramena deux des autobus par
l’autoroute. Le troisième fut abandonné à mi-chemin sur le bord de la piste pour servir
d’abri aux chasseurs et aux trappeurs. Au cours des trente ans qui ont suivi l’abandon du
chantier, la plus grande partie de la route a été détruite par l’eau, l’érosion et les nids de
castors, mais l’autobus est toujours là.
Ce véhicule en ruine – un vieil International Harvester des années 40 – se trouve à 40
kilomètres à l’ouest de Healy à vol d’oiseau. Sa masse incongrue rouille parmi les
mauvaises herbes, juste après la limite du parc national du Denali. Il a perdu son moteur,
plusieurs vitres manquent ou sont cassées et des débris de bouteilles de whisky jonchent
le plancher. Sur sa carrosserie vert et blanc largement oxydée, on peut lire en lettres à la
couleur passée qu’il a appartenu autrefois au Fairbanks City Transit System. C’était
l’autobus 142. De nos jours, il est fréquent que six ou sept mois s’écoulent sans qu’il voie
un être humain. Mais, au début de septembre 1992, six personnes, en trois groupes
distincts, s’approchèrent le même après-midi de ce véhicule abandonné.
En 1980, le parc national du Denali fut agrandi de façon à englober les collines de
Kantishna et les sommets septentrionaux de la Chaîne Extérieure, mais une parcelle
basse, incluse dans la superficie du nouveau parc, fut oubliée. C’est une longue bande de
terrain, appelée « cantons du loup », où passe la première moitié de la piste Stampede.
Comme cette parcelle de 11 kilomètres sur 32 est entourée sur trois côtés par le domaine
protégé du parc national, elle héberge quantité de loups, d’ours, de caribous, d’élans et
autre gibier. Ce secret local est jalousement gardé par les chasseurs et les trappeurs qui le
connaissent. En automne, régulièrement, dès que la saison de l’élan commence, quelques
chasseurs se rendent auprès du vieil autobus qui gît non loin de la rivière Sushana, à
l’extrémité ouest de la bande de terre et à moins de 3 kilomètres de la limite du parc.
Le 6 septembre 1992, Ken Thompson, carrossier à Anchorage, Gordon Samel, son
employé, et leur ami Ferdie Swanson, travailleur du bâtiment, y allèrent pour chasser
l’élan. Ce n’est pas un endroit d’accès facile. À environ 16 kilomètres après la fin de la
partie carrossable de la piste, celle-ci traverse la rivière Teklanika dont les eaux très
froides et rapides sont rendues opaques par les sédiments d’argile glaciaire qu’elle
charrie. La piste descend sur la rive juste en amont d’une gorge étroite que la rivière
franchit en bouillonnant. La perspective de traverser ce torrent laiteux dissuade la plupart
des gens d’aller plus loin.
Mais Thompson, Samel et Swanson sont de vrais habitants de l’Alaska. Ils n’en font
qu’à leur tête et aiment tout particulièrement conduire leurs véhicules dans des endroits
